# A line that starts with the pound symbol is a "comment"
# comments are used to make notes to yourself
# the computer will ignore comments when the program runs


# the "import" command teaches the computer other new commands to use
# the new commands are called "libraries"
import sys


# any letters and number inside quotation marks are called "strings"
# the "print" command makes a string appear on the screen when the program runs
print("Hello World!")


# you can use a + symbol to put multiple strings together
# "sys.argv[1]" is a special command that gets extra data from what the user
#       types into the terminal
print("Hello " + sys.argv[1] + ", how are you?")

